
public class Student {
	private String name;
	private String id;
	private String phone;
	private String address;
	public Student(String Name,String Id,String Phone,String Address) {
		this.name=Name;
		this.id=Id;
		this.phone=Phone;
		this.address=Address;
	}
	public String getName() {
		return name;
	}
	public String getId() {
		return id;
	}
	public String getPhone() {
		return phone;
	}
	public String getAddress() {
		return address;
	}
	
	
}
